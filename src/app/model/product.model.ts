export class Products {
    public name:string;
    public price:string;
    public color:string;
    public size:string;
    public category:string;

    constructor(name:string, price:string, color:string, size:string, category:string) {
        this.name = name;
        this.price = price;
        this.color = color;
        this.size = size;
        this.category = category;
    }

}